package main.java.dia1_variables;

public class Constantes {
    public static void main(String[] args) {
        //Constantes en Java
        final int MI_CONSTANTE = 10;
        System.out.println("MI_CONSTANTE = " + MI_CONSTANTE);

        System.out.println("Constante Math.PI = " + Math.PI);

        //Constante segundos que tiene un minuto
        final int SEGUNDOS_POR_MINUTO = 60;
        System.out.println("SEGUNDOS_POR_MINUTO = " + SEGUNDOS_POR_MINUTO);
    }
}
